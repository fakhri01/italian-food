class Food {
  final int id;
  final String name;
  final String category;
  final String image;
  final double price;
  final int prepTime;
  final double rating;
  final String description;

  Food({
    required this.id,
    required this.name,
    required this.category,
    required this.image,
    required this.price,
    required this.prepTime,
    required this.rating,
    required this.description,
  });
}
